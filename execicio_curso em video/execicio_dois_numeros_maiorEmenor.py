n1 = int(input('Digite o primeiro  numero: '))
n2 = int(input('Digite o segundo numero: '))

if n1 > n2  :

    print('O primeiro n {}  é Maior que o segundo n {}'.format(n1, n2))
elif n1 < n2:
    print('O segundo n {} é Maior que primeiro n {}'.format(n2 , n1))
elif n1 == n2:
    print('Os dois neumeros {} e {} são iguais'.format(n1, n2))
